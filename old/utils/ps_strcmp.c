int	ps_strcmp(const char *s1, const char *s2)
{
	while (*s1 && *s1 != '\n' && *s2)
	{
		if (*s1++ != *s2++)
			return (0);
	}
	if (*s1 != '\n' || *s2 != '\0')
		return (0);
	return (1);
}
